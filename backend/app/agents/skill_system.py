# ─── Skill Registry ────────────────────────────────────────────────────────────

class SkillRegistry:
    """
    Manages skills per workspace.
    Built-in skills always available.
    Custom skills stored in Redis per workspace.
    """

    PREFIX = "goatraw:skills:"

    async def list_skills(self, workspace_id: str) -> List[dict]:
        """List all skills available to a workspace."""
        skills = list(BUILTIN_SKILLS.values())

        # Load custom workspace skills
        r = get_redis()
        custom_key = f"{self.PREFIX}{workspace_id}"
        raw_list = await r.lrange(custom_key, 0, -1)
        for raw in raw_list:
            data = json.loads(raw)
            steps = [SkillStep(**s) for s in data.pop("steps", [])]
            skills.append(SkillDefinition(steps=steps, **data))

        return [s.to_dict() for s in skills]

    async def get_skill(self, skill_id: str, workspace_id: str = None) -> Optional[SkillDefinition]:
        if skill_id in BUILTIN_SKILLS:
            return BUILTIN_SKILLS[skill_id]

        if workspace_id:
            r = get_redis()
            custom_key = f"{self.PREFIX}{workspace_id}"
            raw_list = await r.lrange(custom_key, 0, -1)
            for raw in raw_list:
                data = json.loads(raw)
                if data.get("id") == skill_id:
                    steps = [SkillStep(**s) for s in data.pop("steps", [])]
                    return SkillDefinition(steps=steps, **data)
        return None

    async def install_skill(self, workspace_id: str, skill_def: dict) -> str:
        """Install a custom skill to a workspace."""
        skill_id = skill_def.get("id") or str(uuid.uuid4())[:8]
        skill_def["id"] = skill_id
        r = get_redis()
        await r.lpush(f"{self.PREFIX}{workspace_id}", json.dumps(skill_def))
        logger.info(f"Skill {skill_id} installed to workspace {workspace_id}")
        return skill_id

    async def generate_skill(self, workspace_id: str, description: str) -> SkillDefinition:
        """
        Self-improving feature: Agent generates a new skill from natural language description.
        Equivalent to OpenClaw's autonomous skill creation.
        """
        from app.agents.tools import get_tool_descriptions

        prompt = f"""You are GoatRaw's skill architect. Create a new skill from this description:

"{description}"

Available tools:
{get_tool_descriptions()}

Generate a skill definition as JSON:
{{
  "name": "...",
  "description": "...",
  "category": "lead_gen|research|outreach|monitoring|custom",
  "input_schema": {{"field": "type"}},
  "output_schema": {{"field": "type"}},
  "tags": [],
  "steps": [
    {{
      "tool": "tool_name",
      "params_template": {{"param": "{{input.field}}"}},
      "description": "what this step does"
    }}
  ]
}}

Use {{input.field}} to reference inputs and {{step_N.output.field}} to chain step outputs.
Make it practical and immediately executable."""

        result = await generate_json(prompt, model_type=ModelType.SMART)
        result["id"] = str(uuid.uuid4())[:8]
        result["author"] = f"ai_generated:{workspace_id}"
        result["version"] = "1.0.0"
        result["created_at"] = datetime.utcnow().isoformat()

        steps = [SkillStep(**s) for s in result.pop("steps", [])]
        skill = SkillDefinition(steps=steps, **result)

        # Auto-install to workspace
        skill_dict = skill.to_dict()
        await self.install_skill(workspace_id, skill_dict)
        logger.info(f"AI-generated skill '{skill.name}' created for workspace {workspace_id}")
        return skill


skill_registry = SkillRegistry()
