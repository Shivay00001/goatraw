"""
GoatRaw - Skill System
OpenClaw has SKILL.md files + ClawHub registry.
GoatRaw has SkillPacks stored in DB/Redis, installable per workspace.

A Skill is a composable, reusable agent behavior with:
- Name + description
- Tool sequence (steps)
- Input/output schema
- Optionally self-generated by the agent itself (OpenClaw's "self-improving" feature)
"""

import json
import logging
import uuid
from datetime import datetime
from typing import Any, Dict, List, Optional
from dataclasses import dataclass, field

