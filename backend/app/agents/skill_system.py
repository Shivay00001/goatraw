# ─── Skill Definition ──────────────────────────────────────────────────────────

@dataclass
class SkillStep:
    tool: str
    params_template: dict       # Can reference {input.field} or {step_N.output.field}
    description: str = ""
    condition: Optional[str] = None  # Skip step if condition false


@dataclass
class SkillDefinition:
    id: str
    name: str
    description: str
    category: str              # "lead_gen" | "research" | "outreach" | "monitoring" | "custom"
    steps: List[SkillStep]
    input_schema: dict
    output_schema: dict
    author: str = "system"
    is_public: bool = False
    version: str = "1.0.0"
    created_at: str = field(default_factory=lambda: datetime.utcnow().isoformat())
    tags: List[str] = field(default_factory=list)

    def to_dict(self) -> dict:
        d = self.__dict__.copy()
        d["steps"] = [s.__dict__ for s in self.steps]
        return d


